'use strict'

function getLang()  { return localStorage.getItem('senna-lang')  || 'ar' }
function getTheme() { return localStorage.getItem('senna-theme') || 'light' }

function setLang(l) {
  localStorage.setItem('senna-lang', l)
  document.documentElement.lang = l
  document.documentElement.dir  = l === 'ar' ? 'rtl' : 'ltr'
}
function applyTheme() {
  document.documentElement.classList.toggle('dark', getTheme() === 'dark')
}
function toggleTheme() {
  localStorage.setItem('senna-theme', getTheme() === 'light' ? 'dark' : 'light')
  applyTheme()
}

